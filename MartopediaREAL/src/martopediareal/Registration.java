    private void mobileNOKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mobileNOKeyTyped
        //when the user is typing the mobile number
        if(!Character.isDigit(evt.getKeyChar()))  //this makes sure that the user does not enter any character instead of numbers or mobile number
        {
            evt.consume();  //this will not allow the user to type any character
        }

        if (mobileNO.getText().length() >= 15 )   // this makes sure that the mobile number has not more than 15 numbers
        {
            evt.consume();  //otherwise user will not be able to type more numbers
        }

    }//GEN-LAST:event_mobileNOKeyTyped

