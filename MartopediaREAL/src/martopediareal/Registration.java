    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        //When the submit button is pressed
        String email = emailID.getText();
        String name = Name.getText();
        String mobile = mobileNO.getText();
        String address = Address.getText();
        String Password= Getting_IN.jPasswordField1.getText();  //gets password from the Getting_In form and stores it in this variable

        if(name.isEmpty() && mobile.isEmpty() && address.isEmpty())  //If all these fields are empty then
        {
            jLabel14.setText("*REQUIRED ");     //these labels are placed below the textfields and
            jLabel15.setText("*REQUIRED ");    //will display message REQUIRED if these fields are empty
            jLabel16.setText("*REQUIRED ");   //and the submit button
            jLabel18.setText("*REQUIRED "); // is clicked
        }

        else if(name.isEmpty())
        {
            jLabel18.setText("*REQUIRED ");
        }
        else if(mobile.isEmpty())
        {
            jLabel15.setText("*REQUIRED ");
        }
        else if(address.isEmpty())
        {
            jLabel16.setText("*REQUIRED ");
        }
        else
        {
            try
            {
                Class.forName("java.sql.DriverManager");
                Connection con = (Connection)
                DriverManager.getConnection ("jdbc:mysql://localhost:3306/Martopedia","root", "abc.123$");
                Statement stmt = (Statement) con.createStatement();

                String query="INSERT INTO User_Details VALUES('"+email+"','"+Password+"','"+name+"','"+mobile+"','"+address+"');"; //Inserts all the data given by the user into a table of  the database
                String query2="INSERT INTO AllUsers VALUES('"+email+"','"+Password+"');";  //stores this data into another table of the same database
                stmt.executeUpdate(query);
                stmt.executeUpdate(query2);

                JOptionPane.showMessageDialog(this,"Account Created & Details Registered Successfully!");  //Pop-up message is displayed showing success message
                new UserInterface().setVisible(true); 
                UserInterface.emailLabel.setText(emailID.getText());
                this.dispose();  //current form is closed
            }

            catch(Exception e)
            {
                JOptionPane.showMessageDialog (this, e.getMessage());
            }

        }

    }//GEN-LAST:event_SubmitButtonActionPerformed

