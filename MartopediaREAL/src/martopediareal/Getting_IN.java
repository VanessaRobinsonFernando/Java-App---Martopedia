    private void SignUpButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpButtonActionPerformed
        //When Sign Up button is pressed
        String Email=emailID.getText();  //User's email id is stored in this variable Email and
        String Password=jPasswordField1.getText();  //User's password is stored in this variable Password

        if(Email.isEmpty() && Password.isEmpty())  //This checks if BOTH Email and password fields are empty
        {
            JOptionPane.showMessageDialog(this,"Please enter Email address and Password");
        }

        else if(Email.isEmpty())  //This checks if ONLY Email is empty
        {
            JOptionPane.showMessageDialog(this,"Please enter Email address");
        }

        else if(Password.isEmpty())  //This checks if ONLY Password is empty
        {
            JOptionPane.showMessageDialog(this,"Please enter Password");
        }

        else if(Email.contains("@") && Email.contains("."))  //This checks if the email address comtains @ and . (dot)
        {
            new Registration().setVisible(true);  //form called Registration open
            this.dispose();  //current form is closed
            Registration.emailID.setText(this.emailID.getText());  //Gets the email Id from emailID field in this form and displays it on the emailID field in the Registration form
        }

        else
        {
            JOptionPane.showMessageDialog(null, "Email ID is not valid."+'\n'+"Please Try Again!");  //otherwise this displays the message that email ID is not valid.
            // Please Try Again!
        }
    }//GEN-LAST:event_SignUpButtonActionPerformed

