        // Following happens when Login Buttton is pressed
        String email=emailID.getText();
        String pass=jPasswordField1.getText();

        try
        {
            Connection con = (Connection)  //To establish relation with the database
            DriverManager.getConnection ("jdbc:mysql://localhost:3306/Martopedia","root", "abc.123$");

            String query = "SELECT Email,Password FROM AllUsers WHERE Email= ? AND Password= ?";  //Query to extract the wanted data from database
            pst = con.prepareStatement(query);
            pst.setString(1,emailID.getText());
            pst.setString(2,jPasswordField1.getText());

            ResultSet rs=pst.executeQuery();

            if(rs.next())  //if the entered email ID and pass exists in database then
            {
                new UserInterface().setVisible(true);  //form called UserInterface will open
                 UserInterface.emailLabel.setText(this.emailID.getText());
                this.dispose();  //current form will be closed
            }      
            
          }

        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
